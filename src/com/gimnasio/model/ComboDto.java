package com.gimnasio.model;

/**
 *
 * @author user
 */
public class ComboDto {

    private String codigo;
    private String descripcion;
    private String auxiliar;
    private String assistant;
    private String coAssistant;

    public ComboDto(String codigo, String descripcion) {
        this.codigo = codigo;
        this.descripcion = descripcion;
    }

    public ComboDto(String codigo, String descripcion, String auxiliar) {
        this.codigo = codigo;
        this.descripcion = descripcion;
        this.auxiliar = auxiliar;
    }

    public ComboDto(String codigo, String descripcion, String auxiliar, String assistant) {
        this.codigo = codigo;
        this.descripcion = descripcion;
        this.auxiliar = auxiliar;
        this.assistant = assistant;
    }

    public ComboDto(String codigo, String descripcion, String auxiliar, String assistant, String coAssistant) {
        this.codigo = codigo;
        this.descripcion = descripcion;
        this.auxiliar = auxiliar;
        this.assistant = assistant;
        this.coAssistant = coAssistant;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getAuxiliar() {
        return auxiliar;
    }

    public void setAuxiliar(String auxiliar) {
        this.auxiliar = auxiliar;
    }

    public String getAssistant() {
        return assistant;
    }

    public void setAssistant(String assistant) {
        this.assistant = assistant;
    }

    public String getCoAssistant() {
        return coAssistant;
    }

    public void setCoAssistant(String coAssistant) {
        this.coAssistant = coAssistant;
    }

    @Override
    public String toString() {
        return this.descripcion;
    }
}
